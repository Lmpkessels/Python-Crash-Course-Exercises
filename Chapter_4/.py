# This is a restaurant menu
# Storing a buffet in a Tuple
basic_foods = (
    "Fish and Fries", "Sandwich", "Pancake", "Chicken and Rice",
    "Shoarma")
for food in basic_foods:
    print(food) # Returns all food on the menu


# Storing a new buffet using a tuple
basic_foods = (
    "\nFish and Fries", "Hamburger and Fries", "Hot dog", 
    "Chicken and Rice", "Shoarma")
for food in basic_foods:
    print(food) # Returns all foods on the menu